&nbsp;&nbsp;&nbsp; Конструктивно установка (УВТ) выполнена
в виде
моноблока.<br>
Для выдачи воздуха потребителю УВТ снабжена комплектом гибких
воздуховодов и рассекателем, внутри которого установлен датчик для
измерения параметров воздуха непосредственно на
входе в изделие. <br>
&nbsp;&nbsp;&nbsp; <br>
&nbsp;&nbsp;&nbsp; В комплект поставки УВТ входит
транспортная тара
многоразового
использования (2 ящика) и ЗИП. <br>
<br>
&nbsp;&nbsp;&nbsp; Управление работой УВТ осуществляется в
автоматическом режиме с пульта
управления или в ручном режиме с панели ручного управления. Требуемые
значения расхода воздуха и его
температуры настраиваются перед работой вручную и поддерживаются в
автоматическом режиме в процессе работы установки.<br>
<br>
&nbsp;&nbsp;&nbsp; Контроль за работой УВТ во всех режимах
осуществляется с помощью
